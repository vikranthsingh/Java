package SimpliCode;

public class JavaLogicalOperator {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;
        if (value1 == 1 && value2 == 2){
            System.out.println("value1 == 1 && value2 == 2");
        }
        if (value1 == 1 || value2 == 1){
            System.out.println("value1 == 1 || value2 == value1");
        }
    }
}
