package SimpliCode;

public class JavaIfElse {
    public static void main(String[] args) {
        int marks = 69;
        if (marks >= 90){
            System.out.println("A");
        }else if (marks >= 80){
            System.out.println("B");
        }else if (marks >= 70){
            System.out.println("C");
        }else {
            System.out.println("Fail");
        }
    }
}
