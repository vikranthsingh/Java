package SimpliCode;

public class JavaArithmeticOperator {
    public static void main(String[] args) {
        int result = 1 + 2;
        System.out.println(result);

        result = result -1;
        System.out.println(result);

        result = result * 2;
        System.out.println(result);

        result = result / 2;
        System.out.println(result);

        result = result % 3;
        System.out.println(result);
    }
}
