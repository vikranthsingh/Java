package SimpliCode;

public class JavaDoWhile {
    public static void main(String[] args) {
        int i = 1;
        do {
            //System.out.println("Hi " + i);
            i++;
        } while (i <= 10);

        int a = 10;
        do {
            System.out.println("Hello"+ a);
            a++;
        }while (a<20);
    }
}
