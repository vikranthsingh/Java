package SimpliCode;

public class JavaUnaryOperator {
    public static void main(String[] args) {
        int a = 1;
        System.out.println(a);
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);
        a--;
        System.out.println(a);
        --a;
        System.out.println(a);

        boolean isTrue = true;
        System.out.println(isTrue);
        System.out.println(!isTrue);
    }
}
